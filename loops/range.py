

# if not given a stop argument this will go from 0 to 5
for i in range(5):
    print(i)

# iterates through 5 - 20
print("range with start,stop example:")
for i in range (5,20):
    print(i)

# iterate through 5 - 20 (with a skip of 2)
print("range with start,stop,leap example:")
for i in range (5,20,2):
    print(i)

